//Font Size
double kTitleFontSize(BuildContext context) {
  return kdisplayHeight(context) * 20 / 812;
}

double kDetailPageFontSize(BuildContext context) {
  return kdisplayHeight(context) * 14 / 812;
}

double kListInfoFontSize(BuildContext context) {
  return kdisplayHeight(context) * 11 / 812;
}

double kBrandNameFontSize(BuildContext context) {
  return kdisplayHeight(context) * 16 / 812;
}

double kBtmNaviSelectedFontSize(BuildContext context) {
